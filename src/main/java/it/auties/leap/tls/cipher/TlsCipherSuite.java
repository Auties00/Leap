    public static TlsCipherSuite reservedForPrivateUse(
            int id,
            TlsCipherEngineFactory cipherEngine,
            TlsCipherFactory cipherMode,
            TlsKeyExchangeFactory clientKeyExchange,
            TlsAuthFactory auth,
            TlsHashFactory hash,
            List<TlsVersion> versions,
            boolean secure
    ) {
        if(id >>> 8 != 0xFF) {
            throw new TlsAlert(
                    "Only values from 0xFF00-0xFFFF inclusive are reserved for Private Use",
                    TlsAlertLevel.FATAL,
                    TlsAlertType.INTERNAL_ERROR
            );
        }

        return new TlsCipherSuite(id, cipherEngine, cipherMode, clientKeyExchange, auth, hash, versions, secure);
    }
